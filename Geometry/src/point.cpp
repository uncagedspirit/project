#include <bits/stdc++.h>
using namespace std;

class Point{
    //constructors

    //for 2D point
    Point(double x, double y){
        double onXAxis = x;
        double onYAxis = y;
    }

    //for 3D point
    Point (double x, double y, double z){
        double onXAxis = x;
        double onYAxis = y;
        double onZAxis = z;
    }
};